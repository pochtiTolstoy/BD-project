    c.status,
    c.notes,
    -- адреса: первый (основной)
    a.addr_type,
    a.address   AS addr_raw,
    a.city,
    a.zip_code,
    -- документ: первый
    d.doc_type,
    d.doc_data
FROM customers c
LEFT JOIN LATERAL (
    SELECT * FROM cust_addresses WHERE cust_id = c.cust_id ORDER BY addr_id LIMIT 1
) a ON TRUE
LEFT JOIN LATERAL (
    SELECT * FROM cust_docs WHERE cust_id = c.cust_id ORDER BY doc_id LIMIT 1
) d ON TRUE
ORDER BY c.cust_id;

-- Согласия
SELECT
    c.cust_id,
    c.fullname,
    cn.consent_for,
    cn.agreed,
    cn.dt
FROM customers c
JOIN cust_consents cn ON cn.cust_id = c.cust_id
ORDER BY c.cust_id, cn.consent_id;

-- Дополнительные атрибуты (EAV)
SELECT
    c.cust_id,
    c.fullname,
    e.param_name,
    e.param_val
FROM customers c
JOIN cust_extra e ON e.cust_id = c.cust_id
ORDER BY c.cust_id, e.extra_id;

-- Все адреса (включая дополнительные)
SELECT
    c.cust_id,
    c.fullname,
    a.addr_type,
    a.address,
    a.city,
    a.zip_code
FROM customers c
JOIN cust_addresses a ON a.cust_id = c.cust_id
ORDER BY c.cust_id, a.addr_id;
