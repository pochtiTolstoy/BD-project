DROP TABLE IF EXISTS cust_extra     CASCADE;
DROP TABLE IF EXISTS cust_consents  CASCADE;
DROP TABLE IF EXISTS cust_docs      CASCADE;
DROP TABLE IF EXISTS cust_addresses CASCADE;
DROP TABLE IF EXISTS customers      CASCADE;

-- ============================================================
-- СТРУКТУРА ТАБЛИЦ
-- ============================================================

CREATE TABLE customers (
    cust_id     SERIAL       PRIMARY KEY,
    fullname    VARCHAR(300),       -- ФИО одним полем, регистр не контролировался
    birth_dt    VARCHAR(50),        -- текстовое поле исторически, формат свободный
    sex         VARCHAR(20),        -- М/Ж/male/female/1/0 — зависит от интерфейса ввода
    email       VARCHAR(500),       -- legacy: несколько email в одном поле
    phone       VARCHAR(50),
    phone2      VARCHAR(50),
    inn         VARCHAR(30),        -- формат не валидировался
    snils       VARCHAR(30),        -- формат не валидировался
    marital     VARCHAR(50),
    loyalty_lvl VARCHAR(50),
    login       VARCHAR(150) UNIQUE,
    reg_date    VARCHAR(50),
    status      VARCHAR(30) DEFAULT 'активен',
    notes       TEXT
);

CREATE TABLE cust_addresses (
    addr_id   SERIAL  PRIMARY KEY,
    cust_id   INTEGER NOT NULL REFERENCES customers(cust_id),
    addr_type VARCHAR(50),          -- свободный текст: "дом", "delivery", "доставка"
    address   TEXT,                 -- полная строка адреса без нормализации
    city      VARCHAR(100),
    zip_code  VARCHAR(20)
);

CREATE TABLE cust_docs (
    doc_id   SERIAL  PRIMARY KEY,
    cust_id  INTEGER NOT NULL REFERENCES customers(cust_id),
    doc_type VARCHAR(50),
    doc_data TEXT                   -- серия+номер+кем+когда — всё в одном поле
);

CREATE TABLE cust_consents (
    consent_id  SERIAL  PRIMARY KEY,
    cust_id     INTEGER NOT NULL REFERENCES customers(cust_id),
    consent_for VARCHAR(100),
    agreed      VARCHAR(10),        -- "да"/"нет"/"1"/"0"/"yes"/"true"/"+"/"-" — как пришло
    dt          VARCHAR(50)
);

CREATE TABLE cust_extra (
    extra_id   SERIAL  PRIMARY KEY,
    cust_id    INTEGER NOT NULL REFERENCES customers(cust_id),
    param_name VARCHAR(100),        -- названия ключей не стандартизованы
    param_val  TEXT
);

